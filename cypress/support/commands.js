Cypress.Commands.add('shouldRunTestBasedOnTags', (tags) => {
    const testTags = tags.split(',');
    const currentTestTitle = Cypress.mocha.getRunner().suite.ctx.currentTest.title;
    return testTags.some(tag => currentTestTitle.includes(`[${tag}]`));
  });
  